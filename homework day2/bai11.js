// 10 Use JS Turtle, to draw the following shapes 
//  a. A square
function Square()
    {
        for(let i = 0; i < 4; i++)
        {
            fd(100)
            lt(360/4)
        }
    }
// b. A triangle
function triangle()
    {
        for(let i = 0; i < 3; i++)
            {
                fd(100)
                lt(360/3)
            }
    }
// c. A pentagon 
